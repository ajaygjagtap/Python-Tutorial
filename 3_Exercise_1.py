# Que - Make simple calculator
# Solution -

a = 9
b = 3

# Addition
print("The value of", a, "+", b, "=", a+b)

# Substarction
print("The value of", a, "-", b, "=", a-b)

# Multiplication
print("The value of", a, "*", b, "=", a*b)

# Division
print("The value of", a, "/", b, "=", a/b)

# Modulus
print("The value of", a, "%", b, "=", a%b)

# Exponential
print("The value of", a, "^", 2, "=", a**2)
print("The value of", b, "^", 3, "=", b**3)