# Set - Set is an Unordered collection of data with elements seperated by a comma(,) and enclosed within round {} brackets.
#       Sets are "Immutable" and cannot be modified after creation (Set is Unchangable).
#       Set do not contain duplicate values.
        

set1 = {2, 4, 2, 6}
print(set1)

set2 = {1, 2.3, True, "Red", -2, 0, 3.6 }
print(set2)

# creating empty set
set3 = set()
print(type(set3))

