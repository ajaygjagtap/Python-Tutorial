# Addition Operator
print(9+9)

# Substraction Operator
print(18-9)

# Multiplication Operator
print(9*9)

# Division Operator
print(60/9)

# Floor Division Operator '''Used to print without decimal point'''
print(60//9)

# Modulus Operator '''Used to print remainder'''
print(63%9)

# Exponential Operator '''Used to multiply with the power'''
print(9**3)